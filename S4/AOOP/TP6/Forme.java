public abstract class Forme{
  protected int x;
  protected int y;

  ////////////////// getters & setters //////////////////
  public void setx(int x){
    this.x=x;
  }
  public void sety(int y){
    this.y=y;
  }
  public int getx(){
    return(this.x);
  }
  public int gety(){
    return(this.y);
  }
}
